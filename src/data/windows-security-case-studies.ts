export const kernelSightCaseStudies: CaseStudyReference[] =
  kernelSightCaseStudySlugs.map((slug) => {
    const cveMatch = slug.match(/^CVE-(\d{4})-/i);
    const isSubsystem = slug.endsWith("-deep-dive");
    return {
      slug,
      title: specialTitles[slug] ?? slug.toUpperCase(),
      year: cveMatch ? Number(cveMatch[1]) : null,
      kind: cveMatch ? "CVE" : isSubsystem ? "Subsystem" : "Driver",
      href: `https://splintersfury.github.io/KernelSight/case-studies/${slug}/`,
    };
  });

